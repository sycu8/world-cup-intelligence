/**
 * Generates migrations/0032_fifa_completed_results.sql for finished WC 2026 matches.
 * Run: node scripts/fetch-fifa-wc2026-calendar.mjs && node scripts/generate-fifa-results-migration.mjs
 */
import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { resolvedGroupMatches, resolvedKnockoutMatches } from './fifa-wc2026-official-data.mjs';
import { completedFifaMatches, FIFA_KICKOFF_SOURCE } from './wc2026-fifa-kickoffs.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function sqlStr(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

const byFifaNumber = new Map(
  [...resolvedGroupMatches(), ...resolvedKnockoutMatches()].map((m) => [m.fifaNumber, m]),
);

/** Matches with rich seed data already in earlier migrations — only refresh status/scores. */
const SKIP_RICH_SEED = new Set(['m-w26-ga-1v2']);

const updates = completedFifaMatches()
  .map((row) => {
    const internal = byFifaNumber.get(row.fifaNumber);
    if (!internal) {
      throw new Error(`No internal match for FIFA #${row.fifaNumber}`);
    }
    if (SKIP_RICH_SEED.has(internal.matchId)) {
      return `-- Match ${internal.matchId} (FIFA #${row.fifaNumber}) already seeded with full official data`;
    }
    return [
      'UPDATE matches SET',
      `  status = 'completed',`,
      `  minute = 90,`,
      `  home_score = ${row.homeScore ?? 0},`,
      `  away_score = ${row.awayScore ?? 0},`,
      `  kickoff_utc = ${sqlStr(row.kickoffUtc)},`,
      `  fifa_match_id = ${sqlStr(row.fifaMatchId)},`,
      `  updated_at = datetime('now')`,
      `WHERE id = ${sqlStr(internal.matchId)};`,
    ].join('\n');
  })
  .join('\n\n');

const sql = `-- WC 2026 completed match results from FIFA Match Centre
-- Source: ${FIFA_KICKOFF_SOURCE}
-- Generated by scripts/generate-fifa-results-migration.mjs on ${new Date().toISOString()}

${updates}
`;

const outPath = path.join(__dirname, '..', 'migrations', '0032_fifa_completed_results.sql');
writeFileSync(outPath, sql, 'utf8');
console.log(`Wrote ${outPath} (${completedFifaMatches().length} completed matches)`);
