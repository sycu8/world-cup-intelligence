/**
 * Generates migrations/0020_fifa_official_draw_2026.sql from FIFA official draw data.
 * Run: node scripts/generate-fifa-official-migration.mjs
 */
import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  VENUES,
  LEGACY_TEAM_IDS,
  allTeams,
  resolvedGroupMatches,
  resolvedKnockoutMatches,
} from './fifa-wc2026-official-data.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const TOURNAMENT = 't-2026';

function sqlStr(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function venueInserts() {
  return VENUES.map(
    (v) =>
      `  (${sqlStr(v.id)}, ${sqlStr(v.name)}, ${sqlStr(v.city)}, ${sqlStr(v.country)}, ${v.capacity}, ${sqlStr(v.timezone)})`,
  ).join(',\n');
}

function teamUpserts() {
  const teams = allTeams();
  const inserts = teams.map(
    (t) =>
      `  (${sqlStr(t.id)}, ${sqlStr(t.name)}, ${sqlStr(t.short)}, ${sqlStr(t.iso)}, ${sqlStr(t.confederation)}, 99, 1500, 0.5)`,
  );
  const updates = teams.map(
    (t) =>
      `UPDATE teams SET name = ${sqlStr(t.name)}, short_name = ${sqlStr(t.short)}, country_code = ${sqlStr(t.iso)}, confederation = ${sqlStr(t.confederation)} WHERE id = ${sqlStr(t.id)};`,
  );
  return { inserts: inserts.join(',\n'), updates };
}

function legacyTeamUpdates() {
  const teams = allTeams();
  return Object.entries(LEGACY_TEAM_IDS).map(([name, legacyId]) => {
    const t = teams.find((row) => row.name === name);
    if (!t) {
      throw new Error(`Legacy team not found in draw: ${name}`);
    }
    return `UPDATE teams SET name = ${sqlStr(t.name)}, short_name = ${sqlStr(t.short)}, country_code = ${sqlStr(t.iso)}, confederation = ${sqlStr(t.confederation)} WHERE id = ${sqlStr(legacyId)};`;
  });
}

function groupMatchUpdates() {
  return resolvedGroupMatches().map((m) =>
    [
      'UPDATE matches SET',
      `  home_team_id = ${sqlStr(m.homeTeamId)},`,
      `  away_team_id = ${sqlStr(m.awayTeamId)},`,
      `  kickoff_utc = ${sqlStr(m.kickoffUtc)},`,
      `  venue_id = ${sqlStr(m.venueId)},`,
      `  status = 'scheduled',`,
      '  home_score = 0,',
      '  away_score = 0',
      `WHERE id = ${sqlStr(m.matchId)};`,
    ].join('\n'),
  );
}

function knockoutMatchUpdates() {
  return resolvedKnockoutMatches().map((m) =>
    [
      'UPDATE matches SET',
      `  kickoff_utc = ${sqlStr(m.kickoffUtc)},`,
      `  venue_id = ${sqlStr(m.venueId)},`,
      `  status = 'scheduled',`,
      '  home_score = 0,',
      '  away_score = 0',
      `WHERE id = ${sqlStr(m.matchId)};`,
    ].join('\n'),
  );
}

const { inserts: teamInsertValues, updates: teamUpdates } = teamUpserts();

const sql = `-- FIFA World Cup 2026 official draw (48 teams, 104-match schedule)
-- Source: scripts/fifa-wc2026-official-data.mjs
-- Generated by scripts/generate-fifa-official-migration.mjs

INSERT OR IGNORE INTO venues (id, name, city, country, capacity, timezone)
VALUES
${venueInserts()};

INSERT OR IGNORE INTO teams (id, name, short_name, country_code, confederation, fifa_ranking, elo_rating, collective_strength_rating)
VALUES
${teamInsertValues};

${teamUpdates.join('\n')}

${legacyTeamUpdates().join('\n')}

${groupMatchUpdates().join('\n\n')}

${knockoutMatchUpdates().join('\n\n')}

DELETE FROM probability_snapshots WHERE match_id IN (SELECT id FROM matches WHERE tournament_id = ${sqlStr(TOURNAMENT)});
`;

const out = path.join(__dirname, '..', 'migrations', '0020_fifa_official_draw_2026.sql');
writeFileSync(out, sql, 'utf8');
console.log(`Wrote ${out}`);
console.log(`  venues: ${VENUES.length}`);
console.log(`  teams: ${allTeams().length}`);
console.log(`  group match updates: ${resolvedGroupMatches().length}`);
console.log(`  knockout match updates: ${resolvedKnockoutMatches().length}`);
