/**
 * Generates migrations/0031_fifa_kickoff_times.sql from FIFA Match Centre calendar.
 * Run: node scripts/fetch-fifa-wc2026-calendar.mjs && node scripts/generate-fifa-kickoff-migration.mjs
 */
import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { resolvedGroupMatches, resolvedKnockoutMatches } from './fifa-wc2026-official-data.mjs';
import { FIFA_KICKOFF_SOURCE } from './wc2026-fifa-kickoffs.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function sqlStr(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

const kickoffUpdates = [
  ...resolvedGroupMatches().map(
    (m) => `UPDATE matches SET kickoff_utc = ${sqlStr(m.kickoffUtc)} WHERE id = ${sqlStr(m.matchId)};`,
  ),
  ...resolvedKnockoutMatches().map(
    (m) => `UPDATE matches SET kickoff_utc = ${sqlStr(m.kickoffUtc)} WHERE id = ${sqlStr(m.matchId)};`,
  ),
];

const allResolved = [...resolvedGroupMatches(), ...resolvedKnockoutMatches()];
const fifaIdUpdates = allResolved.map(
  (m) =>
    `UPDATE matches SET fifa_match_id = ${sqlStr(m.fifaMatchId)}, updated_at = datetime('now') WHERE id = ${sqlStr(m.matchId)};`,
);

const sql = `-- WC 2026 kickoff times aligned to FIFA Match Centre (UTC)
-- Source: ${FIFA_KICKOFF_SOURCE}
-- Generated by scripts/generate-fifa-kickoff-migration.mjs

${kickoffUpdates.join('\n\n')}

-- Link all internal matches to FIFA Match Centre ids
${fifaIdUpdates.join('\n')}
`;

const outPath = path.join(__dirname, '..', 'migrations', '0031_fifa_kickoff_times.sql');
writeFileSync(outPath, sql, 'utf8');
console.log(`Wrote ${outPath} (${kickoffUpdates.length} kickoff updates, ${fifaIdUpdates.length} fifa id links)`);
