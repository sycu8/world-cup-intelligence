/**
 * Generates migrations/0021_vn_kickoff_times.sql from Thể Thao 247 VN schedule.
 * Run: node scripts/generate-vn-kickoff-migration.mjs
 */
import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { resolvedGroupMatches, resolvedKnockoutMatches } from './fifa-wc2026-official-data.mjs';
import { VN_KICKOFF_SOURCE } from './wc2026-vn-kickoffs.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function sqlStr(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

const updates = [
  ...resolvedGroupMatches().map(
    (m) => `UPDATE matches SET kickoff_utc = ${sqlStr(m.kickoffUtc)} WHERE id = ${sqlStr(m.matchId)};`,
  ),
  ...resolvedKnockoutMatches().map(
    (m) => `UPDATE matches SET kickoff_utc = ${sqlStr(m.kickoffUtc)} WHERE id = ${sqlStr(m.matchId)};`,
  ),
];

const sql = `-- WC 2026 kickoff times aligned to Vietnam schedule (GMT+7)
-- Source: ${VN_KICKOFF_SOURCE}
-- Generated by scripts/generate-vn-kickoff-migration.mjs

${updates.join('\n\n')}
`;

const outPath = path.join(__dirname, '..', 'migrations', '0021_vn_kickoff_times.sql');
writeFileSync(outPath, sql, 'utf8');
console.log(`Wrote ${outPath} (${updates.length} updates)`);
